//If, ElseIf, and Else Structure

void main(){
 int num1 = 1;
 int num2 = 2;

 //if statement
 if(num1 > num2){
  print("First number is higher!");
 }

 //if-else statement
 if(num1 == num2){
  print("They are equal!");
 }else{
  print("They are not equal!");
 }

 //if-else if statement
 if(num1 > num2){
  print("First number is greater!");
 }else if(num1 < num2){
  print("Second number is greater!");
 }
}

